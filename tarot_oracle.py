import random
import os

class TarotOracle:
    def __init__(self):
        self.tarot_cards = [
            'Шут', 'Маг', 'Верховная Жрица', 'Императрица', 'Император',
            'Иерофант', 'Влюбленные', 'Колесница', 'Сила', 'Отшельник',
            'Колесо Фортуны', 'Справедливость', 'Повешенный', 'Смерть',
            'Умеренность', 'Дьявол', 'Башня', 'Звезда', 'Луна', 'Солнце',
            'Суд', 'Мир'
        ]
        self.zodiac_signs = [
            'Овен', 'Телец', 'Близнецы', 'Рак', 'Лев', 'Дева',
            'Весы', 'Скорпион', 'Стрелец', 'Козерог', 'Водолей', 'Рыбы'
        ]

    def generate_prediction(self):
        card = random.choice(self.tarot_cards)
        zodiac = random.choice(self.zodiac_signs)
        
        predictions = [
            f"Карта {card} говорит, что ваша пицца предвещает неожиданные повороты судьбы. {zodiac} будет вашим путеводителем в этом гастрономическом приключении!",
            f"О, {card}! Ваша пицца раскрывает тайны вселенной. Не удивляйтесь, если {zodiac} принесет вам удачу в ближайшем будущем.",
            f"Интересно... {card} намекает на скрытые возможности. Подумайте о влиянии {zodiac} на ваш выбор начинки для следующей пиццы!",
            f"Мм, {card} в сочетании с энергией {zodiac} предсказывает грядущие перемены. Возможно, пора попробовать новый рецепт пиццы?",
            f"Ах, {card}! Ваша пицца шепчет о грядущих приключениях. {zodiac} советует быть открытым новым вкусовым ощущениям!"
        ]
        
        prediction = random.choice(predictions)
        return card, prediction
